| $\sqrt{x}$ | $\frac{1}{2\sqrt{x}}$ |
| $\frac{1}{x}$ | $-\frac{1}{x^2}$ |
| $e^x$ | $e^x$ |
| $\ln x$ | $\frac{1}{x}$ |
| $\sin x$ | $\cos x$ |
| $\cos x$ | $-\sin x$ |
| $\text{tg}\, x$ | $\frac{1}{\cos^2 x}$ |

### Правила дифференцирования

- $(f \pm g)'' = f'' \pm g''$
- $(cf)'' = cf''$
- $(fg)'' = f''g + fg''$
- $\left(\frac{f}{g}\right)'' = \frac{f''g - fg''}{g^2}$
- $(f(g(x)))'' = f''(g(x)) \cdot g''(x)$ — **цепное правило**

### Геометрический смысл

$f''(x_0)$ — угловой коэффициент касательной к графику $f(x)$ в точке $x_0$.

**Уравнение касательной:**
$$y = f(x_0) + f''(x_0)(x - x_0)$$

### Физический смысл

Если $s(t)$ — путь, то $s''(t) = v(t)$ — скорость, $v''(t) = a(t)$ — ускорение.

### Первообразная

$F''(x) = f(x)$ — $F$ первообразная для $f$.

$$\int_a^b f(x)\,dx = F(b) - F(a)$$

