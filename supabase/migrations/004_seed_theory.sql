## Ключевые понятия

### Область определения $D(f)$

Множество допустимых значений $x$:
- $\frac{1}{g(x)}$ → $g(x) \neq 0$
- $\sqrt{g(x)}$ → $g(x) \geq 0$
- $\ln g(x)$ → $g(x) > 0$

### Чётность и нечётность

- **Чётная:** $f(-x) = f(x)$ — график симметричен относительно оси $Oy$
- **Нечётная:** $f(-x) = -f(x)$ — график симметричен относительно начала координат

### Монотонность

- $f$ **возрастает** на $(a,b)$: $x_1 < x_2 \Rightarrow f(x_1) < f(x_2)$
- $f$ **убывает** на $(a,b)$: $x_1 < x_2 \Rightarrow f(x_1) > f(x_2)$

### Преобразования графиков

| Преобразование | Эффект |
|---|---|
| $f(x) + a$ | Сдвиг вверх на $a$ |
| $f(x) - a$ | Сдвиг вниз на $a$ |
| $f(x + a)$ | Сдвиг влево на $a$ |
| $f(x - a)$ | Сдвиг вправо на $a$ |
| $af(x)$ | Растяжение по $Oy$ в $a$ раз |
| $f(ax)$ | Сжатие по $Ox$ в $a$ раз |
| $-f(x)$ | Отражение относительно $Ox$ |
| $f(-x)$ | Отражение относительно $Oy$ |
| $|f(x)|$ | Часть ниже $Ox$ отражается вверх |
| $f(|x|)$ | Правая часть отражается влево |

