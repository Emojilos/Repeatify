'# Числа и их свойства

## Ключевые понятия

### Делимость

- $a \mid b$ означает $b = ak$ для некоторого целого $k$
- Если $a \mid b$ и $a \mid c$, то $a \mid (b \pm c)$ и $a \mid (b \cdot c)$
- Каждое натуральное число > 1 раскладывается в произведение простых (единственным образом)

### НОД и НОК

- $\text{НОД}(a, b) \cdot \text{НОК}(a, b) = a \cdot b$
- Алгоритм Евклида: $\text{НОД}(a, b) = \text{НОД}(b, a \mod b)$
- $a$ и $b$ взаимно просты $\Leftrightarrow$ $\text{НОД}(a, b) = 1$

### Остатки

- $a \equiv b \pmod{m}$ означает $m \mid (a - b)$
- Остатки можно складывать, умножать и возводить в степень
- **Чётность/нечётность:** чёт + чёт = чёт, нечёт + нечёт = чёт, чёт $\times$ любое = чёт

### Диофантовы уравнения

$ax + by = c$ имеет решения в целых числах $\Leftrightarrow$ $\text{НОД}(a, b) \mid c$

