## Ключевые формулы

### Классическая вероятность

$$P(A) = \frac{m}{n}$$

где $m$ — число благоприятных исходов, $n$ — общее число равновозможных исходов.

### Формулы сложения

- **Несовместные события:** $P(A + B) = P(A) + P(B)$
- **Совместные события:** $P(A + B) = P(A) + P(B) - P(AB)$
- **Противоположное событие:** $P(\bar{A}) = 1 - P(A)$

### Формулы умножения

- **Независимые события:** $P(AB) = P(A) \cdot P(B)$
- **Зависимые события:** $P(AB) = P(A) \cdot P(B|A)$

### Формула полной вероятности

$$P(A) = P(H_1) \cdot P(A|H_1) + P(H_2) \cdot P(A|H_2) + \ldots$$

### Важные приёмы

- **«Хотя бы одно»:** $P(\text{хотя бы 1}) = 1 - P(\text{ни одного})$
- **Дерево вероятностей:** рисуем ветвления для каждого этапа, умножаем по ветвям, складываем нужные

