'# Неравенства

## Ключевые методы

### Метод интервалов

Для рационального неравенства $\frac{P(x)}{Q(x)} \geq 0$:

1. Найти нули числителя и знаменателя
2. Отметить на числовой оси (заштриховать нули знаменателя)
3. Определить знак на каждом интервале
4. Выбрать интервалы по знаку неравенства

### Показательные неравенства

- $a^{f(x)} > a^{g(x)}$:
  - если $a > 1$: $f(x) > g(x)$ (знак сохраняется)
  - если $0 < a < 1$: $f(x) < g(x)$ (знак меняется)

### Логарифмические неравенства

- $\log_a f(x) > \log_a g(x)$:
  - если $a > 1$: $f(x) > g(x) > 0$
  - если $0 < a < 1$: $0 < f(x) < g(x)$
- **Обобщённый метод:** $(\log_a f(x) - \log_a g(x)) \cdot (a - 1) > 0$ при переходе к знаку

### Метод рационализации (метод замены множителей)

$$\log_{h(x)} f(x) > \log_{h(x)} g(x) \Leftrightarrow (h(x) - 1)(f(x) - g(x)) > 0$$

при условии $f(x) > 0$, $g(x) > 0$, $h(x) > 0$, $h(x) \neq 1$.

### Системы неравенств

Решение — пересечение множеств решений каждого неравенства.

